/**
题目：打印出所有的"水仙花数"，所谓"水仙花数"是指一个三位数，其各位数字立方和
      等于该数本身。例如：153是一个"水仙花数"，因为153=1的三次方＋5的三次方＋
      3的三次方。
分析:如何把每一位提出来？x%100/10取十位  x%10/1取个位
     水仙花是三位数，即从100~99
     for循环，if
**/
#include <stdio.h>
int main(void)
{
    int i;
    int j,k,l;
    for(i=100; i<1000; i++)
    {
        j = i%1000/100; //百位
        k = i%100/10;   //十位
        l = i%10/1;     //个位
        if( (j*j*j+k*k*k+l*l*l)==i )
        {
            printf("%d\n", i);
        }
    }
}
