/**
题目:古典问题（兔子生崽）：有一对兔子，从出生后第3个月起每个月
     都生一对兔子小兔子长到第三个月后每个月又生一对兔子，假如
     兔子都不死，问每个月的兔子总数为多少？（输出前40个月即可）
分析:根据数学运算。生孩子的数为 1,1,2,3,5,8,13,21....
     得出规律，从第三个月开始，每个月都等于上两个月之和
**/
#include<stdio.h>

int main()
{
  int M1=1, M2=1;
  int i;
  for(i=1; i<=20; i++)//打印20次
  {
      printf("%12d%12d", M1, M2);
      M1 = M1+M2;
      M2 = M1+M2;

      if(i%2 == 0)
        {
            printf("\n");
        }
  }
  return 0;
}
